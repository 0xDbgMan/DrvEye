"""PowerShell check script generation."""
from __future__ import annotations

import datetime
import os
from typing import TYPE_CHECKING

from drivertool.ioctl import decode_ioctl

if TYPE_CHECKING:
    from drivertool.pe_analyzer import PEAnalyzer
    from drivertool.scanner import VulnScanner


def generate_check_script(pe_info: dict, scanner: "VulnScanner",
                         pe_analyzer: "PEAnalyzer",
                         output_dir: str) -> str:
    """
    Generate a PowerShell script that checks on a Windows machine whether:
    - The driver file exists on disk
    - A service is registered for it
    - The driver is currently loaded in kernel
    - The Authenticode signature is valid
    - Secure Boot & HVCI status
    - The driver is on Microsoft's block list
    - Can load/unload the driver for testing
    """
    driver_filename = os.path.basename(pe_info.get("filepath", "driver.sys"))
    driver_base = os.path.splitext(driver_filename)[0]
    sha256 = pe_info.get("sha256", "")
    ci = pe_info.get("certificate", {})
    signer_cn = ci.get("signer_cn", "Unknown")

    # Build the device names list for handle-open test
    device_names = pe_analyzer.device_names
    device_open_tests = ""
    for dn in device_names:
        # Convert kernel path to usermode path
        user_path = dn
        if dn.startswith("\\Device\\"):
            user_path = "\\\\.\\" + dn.split("\\")[-1]
        elif dn.startswith("\\DosDevices\\") or dn.startswith("\\??\\"):
            user_path = "\\\\.\\" + dn.split("\\")[-1]
        device_open_tests += f'''
    # Test device handle: {dn}
    try {{
        $handle = [System.IO.File]::Open("{user_path}", [System.IO.FileMode]::Open,
                    [System.IO.FileAccess]::Read, [System.IO.FileShare]::ReadWrite)
        Write-Host "  [+] Device accessible: {user_path}" -ForegroundColor Green
        $handle.Close()
    }} catch {{
        Write-Host "  [-] Device NOT accessible: {user_path} ($($_.Exception.Message))" -ForegroundColor Yellow
    }}
'''

    # Build IOCTL test list
    ioctl_tests = ""
    for code in scanner.ioctl_codes[:10]:  # Limit to first 10
        purpose = scanner.ioctl_purposes.get(code, "")
        p_str = f" ({purpose})" if purpose else ""
        ioctl_tests += f'    Write-Host "    IOCTL: 0x{code:08X}{p_str}"\n'

    script = f'''#Requires -RunAsAdministrator
<#
.SYNOPSIS
    Driver Check Script — Generated by TheDebugger
    Checks if {driver_filename} is loaded, signed, and loadable on this system.

.DESCRIPTION
    Auto-generated script for driver: {driver_filename}
    SHA-256: {sha256}
    Signer : {signer_cn}

.NOTES
    Generated: {datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S")}
    Run this script as Administrator on the target Windows machine.
#>

param(
    [string]$DriverPath = "",
    [switch]$Load,
    [switch]$Unload,
    [switch]$Detailed
)

$ErrorActionPreference = "Continue"
$driverName = "{driver_base}"
$driverFile = "{driver_filename}"
$expectedHash = "{sha256}"

Write-Host ""
Write-Host "========================================" -ForegroundColor Cyan
Write-Host "  TheDebugger — Driver Check Script" -ForegroundColor Cyan
Write-Host "  Target: $driverFile" -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
Write-Host ""

# ── 1. System Information ──────────────────────────────────────────────────
Write-Host "[*] System Information:" -ForegroundColor White
Write-Host "  OS       : $([System.Environment]::OSVersion.VersionString)"
Write-Host "  Arch     : $(if ([System.Environment]::Is64BitOperatingSystem) {{ 'x64' }} else {{ 'x86' }})"
Write-Host "  Computer : $env:COMPUTERNAME"
Write-Host ""

# ── 2. Secure Boot Status ─────────────────────────────────────────────────
Write-Host "[*] Security Policy:" -ForegroundColor White
try {{
    $sb = Confirm-SecureBootUEFI
    if ($sb) {{
        Write-Host "  [!] Secure Boot: ENABLED" -ForegroundColor Yellow
        Write-Host "      Unsigned/blocked drivers will NOT load" -ForegroundColor Yellow
    }} else {{
        Write-Host "  [+] Secure Boot: DISABLED" -ForegroundColor Green
    }}
}} catch {{
    Write-Host "  [?] Secure Boot: Could not determine (non-UEFI or access denied)" -ForegroundColor Gray
}}

# HVCI / Memory Integrity
try {{
    $dg = Get-CimInstance -ClassName Win32_DeviceGuard -Namespace root\\Microsoft\\Windows\\DeviceGuard -ErrorAction Stop
    $hvci = $dg.SecurityServicesRunning -contains 2
    if ($hvci) {{
        Write-Host "  [!] HVCI (Memory Integrity): ENABLED" -ForegroundColor Yellow
        Write-Host "      Non-HVCI-compatible drivers will be BLOCKED" -ForegroundColor Yellow
    }} else {{
        Write-Host "  [+] HVCI (Memory Integrity): DISABLED" -ForegroundColor Green
    }}
    $cg = $dg.SecurityServicesRunning -contains 1
    if ($cg) {{
        Write-Host "  [*] Credential Guard: ENABLED" -ForegroundColor Cyan
    }}
}} catch {{
    Write-Host "  [?] HVCI: Could not query Device Guard" -ForegroundColor Gray
}}

# Test signing mode
try {{
    $bcd = bcdedit /enum {{current}} 2>$null | Select-String "testsigning"
    if ($bcd -match "Yes") {{
        Write-Host "  [!] Test Signing: ENABLED" -ForegroundColor Yellow
    }} else {{
        Write-Host "  [*] Test Signing: DISABLED (normal)" -ForegroundColor Green
    }}
}} catch {{
    Write-Host "  [?] Test Signing: Could not determine" -ForegroundColor Gray
}}

# Debug mode
try {{
    $bcd = bcdedit /enum {{current}} 2>$null | Select-String "debug"
    if ($bcd -match "Yes") {{
        Write-Host "  [!] Kernel Debug: ENABLED" -ForegroundColor Yellow
    }}
}} catch {{}}
Write-Host ""

# ── 3. Driver File Search ─────────────────────────────────────────────────
Write-Host "[*] Searching for $driverFile on disk:" -ForegroundColor White
$searchPaths = @(
    "$env:SystemRoot\\System32\\drivers\\$driverFile",
    "$env:SystemRoot\\System32\\$driverFile",
    "$env:SystemRoot\\SysWOW64\\drivers\\$driverFile",
    "$env:TEMP\\$driverFile"
)
if ($DriverPath) {{ $searchPaths = @($DriverPath) + $searchPaths }}

$foundPath = $null
foreach ($p in $searchPaths) {{
    if (Test-Path $p) {{
        $foundPath = $p
        $fileInfo = Get-Item $p
        Write-Host "  [+] FOUND: $p" -ForegroundColor Green
        Write-Host "      Size     : $($fileInfo.Length) bytes"
        Write-Host "      Modified : $($fileInfo.LastWriteTime)"

        # Hash check
        $hash = (Get-FileHash $p -Algorithm SHA256).Hash.ToLower()
        Write-Host "      SHA-256  : $hash"
        if ($hash -eq $expectedHash) {{
            Write-Host "      Hash Match: YES (matches analyzed driver)" -ForegroundColor Green
        }} else {{
            Write-Host "      Hash Match: NO (different version!)" -ForegroundColor Yellow
        }}
        break
    }}
}}
if (-not $foundPath) {{
    Write-Host "  [-] Not found in standard locations" -ForegroundColor Yellow
    # Broader search
    Write-Host "  [*] Searching entire System32..." -ForegroundColor Gray
    $found = Get-ChildItem "$env:SystemRoot\\System32" -Recurse -Filter $driverFile -ErrorAction SilentlyContinue | Select-Object -First 1
    if ($found) {{
        $foundPath = $found.FullName
        Write-Host "  [+] FOUND: $foundPath" -ForegroundColor Green
    }} else {{
        Write-Host "  [-] Driver file not found on disk" -ForegroundColor Red
    }}
}}
Write-Host ""

# ── 4. Authenticode Signature Check ────────────────────────────────────────
if ($foundPath) {{
    Write-Host "[*] Signature Verification:" -ForegroundColor White
    $sig = Get-AuthenticodeSignature $foundPath
    Write-Host "  Status      : $($sig.Status)"
    Write-Host "  Status Msg  : $($sig.StatusMessage)"
    if ($sig.SignerCertificate) {{
        $cert = $sig.SignerCertificate
        Write-Host "  Signer      : $($cert.Subject)"
        Write-Host "  Issuer      : $($cert.Issuer)"
        Write-Host "  Serial      : $($cert.SerialNumber)"
        Write-Host "  Thumbprint  : $($cert.Thumbprint)"
        Write-Host "  Valid From  : $($cert.NotBefore)"
        Write-Host "  Valid Until : $($cert.NotAfter)"
        if ($cert.NotAfter -lt (Get-Date)) {{
            Write-Host "  [!] EXPIRED" -ForegroundColor Red
        }}
    }}
    if ($sig.TimeStamperCertificate) {{
        $ts = $sig.TimeStamperCertificate
        Write-Host "  Timestamp   : $($ts.Subject)"
        Write-Host "  TS Valid    : $($ts.NotBefore) → $($ts.NotAfter)"
    }}
    Write-Host ""
}}

# ── 5. Service / Driver Registration ──────────────────────────────────────
Write-Host "[*] Service Registration:" -ForegroundColor White
$svc = Get-Service -Name $driverName -ErrorAction SilentlyContinue
if ($svc) {{
    Write-Host "  [+] Service found: $($svc.Name)" -ForegroundColor Green
    Write-Host "      Display  : $($svc.DisplayName)"
    Write-Host "      Status   : $($svc.Status)"
    Write-Host "      StartType: $($svc.StartType)"

    # Get more details via WMI
    $wmiSvc = Get-WmiObject Win32_SystemDriver -Filter "Name='$driverName'" -ErrorAction SilentlyContinue
    if ($wmiSvc) {{
        Write-Host "      Path     : $($wmiSvc.PathName)"
        Write-Host "      State    : $($wmiSvc.State)"
        Write-Host "      Started  : $($wmiSvc.Started)"
    }}
}} else {{
    Write-Host "  [-] No service registered as '$driverName'" -ForegroundColor Yellow

    # Broader search
    $matches = Get-WmiObject Win32_SystemDriver | Where-Object {{
        $_.PathName -like "*$driverFile*" -or $_.Name -like "*$driverName*"
    }}
    if ($matches) {{
        foreach ($m in $matches) {{
            Write-Host "  [+] Found matching driver service:" -ForegroundColor Green
            Write-Host "      Name  : $($m.Name)"
            Write-Host "      Path  : $($m.PathName)"
            Write-Host "      State : $($m.State)"
        }}
    }} else {{
        Write-Host "  [-] No matching driver service found in WMI" -ForegroundColor Yellow
    }}
}}
Write-Host ""

# ── 6. Loaded Kernel Modules Check ────────────────────────────────────────
Write-Host "[*] Loaded Kernel Modules:" -ForegroundColor White
$loaded = driverquery /v /fo csv 2>$null | ConvertFrom-Csv | Where-Object {{
    $_."Module Name" -like "*$driverName*" -or
    $_."Display Name" -like "*$driverName*"
}}
if ($loaded) {{
    foreach ($drv in $loaded) {{
        Write-Host "  [+] LOADED in kernel!" -ForegroundColor Green
        Write-Host "      Module : $($_.'Module Name')"
        Write-Host "      Display: $($_.'Display Name')"
        Write-Host "      Type   : $($_.'Driver Type')"
        Write-Host "      State  : $($_.State)"
        Write-Host "      Path   : $($_.Path)"
    }}
}} else {{
    Write-Host "  [-] Driver is NOT currently loaded" -ForegroundColor Yellow
}}

# Also check via NtQuerySystemInformation (loaded modules)
try {{
    $modules = Get-CimInstance -ClassName Win32_SystemDriver -ErrorAction Stop |
        Where-Object {{ $_.PathName -like "*$driverFile*" -and $_.Started -eq $true }}
    if ($modules) {{
        Write-Host "  [+] Confirmed loaded via WMI" -ForegroundColor Green
    }}
}} catch {{}}
Write-Host ""

# ── 7. Device Object Accessibility ────────────────────────────────────────
Write-Host "[*] Device Handle Tests:" -ForegroundColor White
{device_open_tests}

# ── 8. Known IOCTL Codes ──────────────────────────────────────────────────
Write-Host "[*] Known IOCTL Codes for this driver:" -ForegroundColor White
{ioctl_tests}
Write-Host ""

# ── 9. Microsoft Block List Check ─────────────────────────────────────────
Write-Host "[*] Microsoft Block List:" -ForegroundColor White
if ($foundPath) {{
    # Check against CI policy
    try {{
        $cipolicy = Get-SystemDriver -ScanPath $foundPath -ErrorAction Stop
        if ($cipolicy.IsBlocked) {{
            Write-Host "  [!!] BLOCKED by Microsoft driver block policy!" -ForegroundColor Red
        }} else {{
            Write-Host "  [+] Not in local block policy" -ForegroundColor Green
        }}
    }} catch {{
        Write-Host "  [?] Could not check CI policy (cmdlet not available)" -ForegroundColor Gray
    }}
}}
Write-Host ""

# ── 10. Load / Unload (optional) ──────────────────────────────────────────
if ($Load -and $foundPath) {{
    Write-Host "[*] Attempting to LOAD driver..." -ForegroundColor Cyan
    try {{
        # Create service if not exists
        if (-not (Get-Service $driverName -ErrorAction SilentlyContinue)) {{
            sc.exe create $driverName type=kernel binPath=$foundPath start=demand 2>&1 | Out-Null
            Write-Host "  [*] Service created: $driverName" -ForegroundColor Cyan
        }}
        sc.exe start $driverName 2>&1
        $result = $LASTEXITCODE
        if ($result -eq 0) {{
            Write-Host "  [+] Driver LOADED successfully!" -ForegroundColor Green
        }} else {{
            Write-Host "  [-] Load FAILED (exit code: $result)" -ForegroundColor Red
            Write-Host "      Check Event Viewer > System log for details" -ForegroundColor Yellow
        }}
    }} catch {{
        Write-Host "  [-] Error: $($_.Exception.Message)" -ForegroundColor Red
    }}
}}

if ($Unload) {{
    Write-Host "[*] Attempting to UNLOAD driver..." -ForegroundColor Cyan
    try {{
        sc.exe stop $driverName 2>&1
        sc.exe delete $driverName 2>&1
        Write-Host "  [+] Driver unloaded and service removed" -ForegroundColor Green
    }} catch {{
        Write-Host "  [-] Error: $($_.Exception.Message)" -ForegroundColor Red
    }}
}}

# ── 11. Event Log Check ───────────────────────────────────────────────────
if ($Detailed) {{
    Write-Host ""
    Write-Host "[*] Recent driver-related Event Log entries:" -ForegroundColor White
    try {{
        $events = Get-WinEvent -FilterHashtable @{{
            LogName = 'System'
            ProviderName = 'Microsoft-Windows-Kernel-General',
                          'Microsoft-Windows-CodeIntegrity',
                          'Service Control Manager'
            StartTime = (Get-Date).AddDays(-7)
        }} -MaxEvents 20 -ErrorAction Stop | Where-Object {{
            $_.Message -like "*$driverName*" -or
            $_.Message -like "*$driverFile*" -or
            $_.Id -in @(7000, 7001, 7009, 7034, 7045, 3001, 3002, 3003, 3004)
        }}
        foreach ($evt in $events) {{
            $color = if ($evt.LevelDisplayName -eq "Error") {{ "Red" }}
                     elseif ($evt.LevelDisplayName -eq "Warning") {{ "Yellow" }}
                     else {{ "Gray" }}
            Write-Host "  [$($evt.TimeCreated.ToString('yyyy-MM-dd HH:mm:ss'))] [$($evt.LevelDisplayName)] $($evt.Message.Substring(0, [Math]::Min(120, $evt.Message.Length)))..." -ForegroundColor $color
        }}
        if (-not $events) {{
            Write-Host "  No recent events found for this driver" -ForegroundColor Gray
        }}
    }} catch {{
        Write-Host "  [?] Could not query event logs: $($_.Exception.Message)" -ForegroundColor Gray
    }}
}}

Write-Host ""
Write-Host "========================================" -ForegroundColor Cyan
Write-Host "  Check complete." -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
'''

    # Write the script
    os.makedirs(output_dir, exist_ok=True)
    script_name = f"check_{driver_base}.ps1"
    script_path = os.path.join(output_dir, script_name)
    with open(script_path, "w", encoding="utf-8") as f:
        f.write(script)
    return script_path
